package Array;

public class Array1 {

    public static void main(String[] args) {
        int[] marks = {95, 97, 98}; // Declare and initialize the array in one line.
        
        // Print all values in the array using a loop.
        for (int i = 0; i < marks.length; i++) {
            System.out.println(marks[i]);
        }
    }
}
